package mueblerialacaprichosa;

/**
 *
 * @author Luis
 */
public class Objeto {
    
    private Integer CR;
    private String Descripcion;
    private String Diseñador;
    private String FechaR;
    private String Categoria;
    private String Modelo;
    
    public Objeto(){
        
    }

    /**
     * @return the CR
     */
    public Integer getCR() {
        return CR;
    }

    /**
     * @param CR the CR to set
     */
    public void setCR(Integer CR) {
        this.CR = CR;
    }

    /**
     * @return the Descripcion
     */
    public String getDescripcion() {
        return Descripcion;
    }

    /**
     * @param Descripcion the Descripcion to set
     */
    public void setDescripcion(String Descripcion) {
        this.Descripcion = Descripcion;
    }

    /**
     * @return the Diseñador
     */
    public String getDiseñador() {
        return Diseñador;
    }

    /**
     * @param Diseñador the Diseñador to set
     */
    public void setDiseñador(String Diseñador) {
        this.Diseñador = Diseñador;
    }

    /**
     * @return the FechaR
     */
    public String getFechaR() {
        return FechaR;
    }

    /**
     * @param FechaR the FechaR to set
     */
    public void setFechaR(String FechaR) {
        this.FechaR = FechaR;
    }

    /**
     * @return the Categoria
     */
    public String getCategoria() {
        return Categoria;
    }

    /**
     * @param Categoria the Categoria to set
     */
    public void setCategoria(String Categoria) {
        this.Categoria = Categoria;
    }

    /**
     * @return the Modelo
     */
    public String getModelo() {
        return Modelo;
    }

    /**
     * @param Modelo the Modelo to set
     */
    public void setModelo(String Modelo) {
        this.Modelo = Modelo;
    }
    
}
